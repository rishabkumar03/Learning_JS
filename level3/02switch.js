const month = "f"

switch (month) {
    case "j":
        console.log("jan");
        
        break;
    case "f":
        console.log("feb");
        
        break;
    case "m":
        console.log("mar");
        
        break;
    case "a":
        console.log("apr");
        
        break;

    default:
        console.log("other than first four months");
        
        break;
}